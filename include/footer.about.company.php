Seintex (Сеинтекс) – производит
                    автомобильные аксессуары для 
                    иномарок и отечественных 
                    автомобилей. Основные виды 
                    продукции: резиновые коврики в 
                    салон, ворсовые коврики, 3D 
                    коврики, полимерные коврики в 
                    багажник, коврики для грузовых 
                    автомобилей, брызговики, 
                    автомобильные чехлы.