<h2>
	Компания «Сеинтекс» 
</h2>
<p>
	Динамично развивающееся производственное предприятие, действующее с 1997 года. С 2000 года предприятие основывается на собственной промышленной площадке. В настоящее время компания располагает необходимыми технологиями, оборудованием и квалифицированным персоналом для обеспечения полного технологического цикла от производства сырья и оснастки до конечной продукции.
</p>